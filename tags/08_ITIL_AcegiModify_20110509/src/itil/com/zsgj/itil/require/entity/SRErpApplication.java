package com.zsgj.itil.require.entity;

import com.zsgj.info.framework.dao.BaseObject;

/**
 * ERP非常规传输系统
 * @Class Name SRErpApplication
 * @Author lee
 * @Create In Dec 8, 2009
 */
public class SRErpApplication extends BaseObject{
	private Long id;
	private String name;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public boolean equals(Object obj) {
		if (this == obj)
   		return true;
   	if (!super.equals(obj))
   		return false;
   	if (getClass() != obj.getClass())
   		return false;
   	final SRErpApplication other = (SRErpApplication) obj;
   	if (id == null) {
   		if (other.id != null)
   			return false;
   	} else if (!id.equals(other.id))
   		return false;
   	return true; 
   }
   public int hashCode() {
		final int prime = 31;
   	int result = super.hashCode();
   	result = prime * result + ((id == null) ? 0 : id.hashCode());
   	return result;
  	}
}
